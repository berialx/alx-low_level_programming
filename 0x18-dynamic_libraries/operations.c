/**
 * add - addition
 * @a: 1st number
 * @b: 2nd number
 * Return: result
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - subraction
 * @a: 1st number
 * @b: 2nd number
 * Return: result
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - multiplication
 * @a: 1st number
 * @b: 2nd number
 * Return: result
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - division
 * @a: 1st number
 * @b: 2nd number
 * Return: result
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - modulo
 * @a: 1st number
 * @b: 2nd number
 * Return: result
 */
int mod(int a, int b)
{
	return (a % b);
}
